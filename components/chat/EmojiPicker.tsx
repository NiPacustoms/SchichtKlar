interface EmojiPickerProps {
  onSelect: (emoji: string) => void;
  onClose?: () => void;
}

export default function EmojiPicker({ onSelect, onClose }: EmojiPickerProps) {
  const [selectedCategory, setSelectedCategory] = useState<string>(
    Object.keys(EMOJI_CATEGORIES)[0]
  );

  return (
    <Paper
      elevation={8}
      sx={{
        position: 'absolute',
        bottom: '100%',
        left: 0,
        mb: 1,
        width: 320,
        maxHeight: 400,
        overflow: 'hidden',
        display: 'flex',
        flexDirection: 'column',
        zIndex: 1000,
      }}
    >
      {/* Category Tabs */}
      <Box sx={{ display: 'flex', borderBottom: 1, borderColor: 'divider', overflowX: 'auto' }}>
        {Object.keys(EMOJI_CATEGORIES).map(category => (
          <Box
            key={category}
            onClick={() => setSelectedCategory(category)}
            sx={{
              px: 2,
              py: 1,
              cursor: 'pointer',
              borderBottom: selectedCategory === category ? 2 : 0,
              borderColor: 'primary.main',
              bgcolor: selectedCategory === category ? 'rgba(0,0,0,0.05)' : 'transparent',
              '&:hover': {
                bgcolor: 'rgba(0,0,0,0.05)',
              },
            }}
          >
            <Typography variant="caption" sx={{ fontSize: '0.7rem', whiteSpace: 'nowrap' }}>
              {category.split(' ')[0]}
            </Typography>
          </Box>
        ))}
      </Box>

      {/* Emoji Grid */}
      <Box sx={{ flex: 1, overflowY: 'auto', p: 1 }}>
        <Box
          sx={{
            display: 'grid',
            gridTemplateColumns: 'repeat(8, 1fr)',
            gap: 0.5,
          }}
        >
          {EMOJI_CATEGORIES[selectedCategory as keyof typeof EMOJI_CATEGORIES].map(emoji => (
            <Box
              key={emoji}
              onClick={() => {
                onSelect(emoji);
                onClose?.();
              }}
              sx={{
                fontSize: '1.5rem',
                p: 1,
                cursor: 'pointer',
                display: 'flex',
                alignItems: 'center',
                justifyContent: 'center',
                borderRadius: 1,
                '&:hover': {
                  bgcolor: 'rgba(0,0,0,0.1)',
                },
              }}
            >
              {emoji}
            </Box>
          ))}
        </Box>
      </Box>
    </Paper>
  );
}
